# The word polymorphism means having many forms. 
# In programming, polymorphism means same function name (but different signatures) being uses for different types.

# Example -->>
# A simple Python function to demonstrate 
# Polymorphism 

def add(x, y, z = 0): 
	return x + y+z 

# Driver code 
print(add(2, 3)) 
print(add(2, 3, 4)) 

